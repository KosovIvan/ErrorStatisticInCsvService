
using ErrorStatisticService.Data;
using ErrorStatisticService.Models;
using ErrorStatisticService.Services;

namespace ErrorStatisticService
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            builder.Services.AddControllers();
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            builder.Services.AddSingleton<IRandomGeneratorService<ErrorLog>, RandomErrorLogGeneratorService>();
            builder.Services.AddScoped<CsvErrorLogsFile>();

            var app = builder.Build();

            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();

            app.MapControllers();

            app.Run();
        }
    }
}